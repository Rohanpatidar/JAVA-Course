package JAVACourse;

/**
 * The _02_DataType class demonstrates the use of various primitive data types in Java.
 * It covers the following categories of data types:
 * <p>
 * - Integer types: Includes byte, short, int, and long to represent whole numbers.
 * - Floating-point types: Includes float and double for numbers with fractional parts.
 * - Character type: A single 16-bit Unicode character is represented using char.
 * - Boolean type: Represents true or false values with a boolean variable.
 * <p>
 * Each type is initialized with an example value, showcasing valid assignments
 * and the syntax for using these data types in the Java programming language.
 */
public class _02_DataType {
    public static void main(String[] args) {
        /*
        Integer --> Byte,Short,Int,long
        Float   --> Double(By default),Float
        Character --> char
        Boolean --> boolean
        */

        int num1 = 9;
        byte by = 127;
        short shy = 256;
        long l = 58689L;

        float f = 5.8f;
        double d = 58.2;

        char c = 'k';

        boolean b = true;

    }
}
