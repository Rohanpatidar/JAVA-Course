
package JAVACourse;

/**
 * The _29_Calc class provides basic arithmetic operations.
 * It includes methods for addition and subtraction of two integers.
 * <p>
 * This class serves as a foundational utility for performing
 * simple mathematical calculations.
 */
public class _29_Calc {
    public int add(int n1, int n2) {
        return n1 + n2;
    }

    public int sub(int n1, int n2) {
        return n1 - n2;
    }
}
