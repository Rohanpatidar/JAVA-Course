package JAVACourse;

/**
 * This class demonstrates the concept of variables in Java.
 * A variable is used to store data that a program can manipulate.
 * In this example, an integer variable is defined and its value is printed to the console.
 */
public class _01_variable {
    public static void main(String[] args) {
        int num = 6; // num is variable
        System.out.println(num);
    }
}
